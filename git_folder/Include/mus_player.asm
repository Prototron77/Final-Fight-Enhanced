;-------------------------------------------------------------------------------------------------------------
;	 MUSIC PLAYER - ROUTINE
;-------------------------------------------------------------------------------------------------------------

;------------------------------------------------------
;		Call P61_Init
;------------------------------------------------------

	lea 	MUS_L1,a0
	sub.l 	a1,a1
	sub.l 	a2,a2
	moveq 	#0,d0
	jsr 	P61_Init

;------------------------------------------------------
;		Frame loop start 
;------------------------------------------------------

WAIT:	
	move	$1e(a6),d0					; Wait for Copper-bit in INTREQR
	and.l	#$10,d0
	beq.b	WAIT
	move	#$10,$9c(a6)				; Clear the bit (poll)

	ifeq 	p61cia
		move	#$7f3,$180(a6)
		jsr 	P61_Music				; and call the playroutine manually.
		move	#$003,$180(a6)
	endc

Playrtn:
	include "P6112-Play.i"



